    "def compute_sharpe_ratio(returns, risk_free_rate=0.0):\n",
    "    if len(returns) < 2:\n",
    "        return 0.0\n",
    "    excess = returns - risk_free_rate / 252\n",
    "    std = np.std(excess)\n",
    "    if std == 0:\n",
    "        return 0.0\n",
    "    return float(np.mean(excess) / std * np.sqrt(252))\n",
    "\n",
    "def compute_max_drawdown(cumulative_returns):\n",
    "    if len(cumulative_returns) < 2:\n",
    "        return 0.0\n",
    "    peak = cumulative_returns.expanding().max()\n",
    "    drawdown = (cumulative_returns - peak) / peak\n",
    "    return float(drawdown.min())\n",
    "\n",
    "def compute_win_rate(strategy_returns):\n",
    "    if len(strategy_returns) == 0:\n",
    "        return 0.0\n",
    "    wins = (strategy_returns > 0).sum()\n",
    "    return float(wins / len(strategy_returns))\n",
    "\n",
    "def signals_to_strategy_returns(predictions, actual_returns, transaction_cost=0.0002):\n",
